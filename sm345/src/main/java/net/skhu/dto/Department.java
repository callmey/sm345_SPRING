package net.skhu.dto;

public class Department {

	int department_id;
	String department_name;
	int department_number;
	String department_phone;
	int department_employee;
	int department_professor;

	public int getDepartment_id() {
		return department_id;
	}
	public void setDepartment_id(int department_id) {
		this.department_id = department_id;
	}
	public String getDepartment_name() {
		return department_name;
	}
	public void setDepartment_name(String department_name) {
		this.department_name = department_name;
	}
	public int getDepartment_number() {
		return department_number;
	}
	public void setDepartment_number(int department_number) {
		this.department_number = department_number;
	}
	public String getDepartment_phone() {
		return department_phone;
	}
	public void setDepartment_phone(String department_phone) {
		this.department_phone = department_phone;
	}
	public int getDepartment_employee() {
		return department_employee;
	}
	public void setDepartment_employee(int department_employee) {
		this.department_employee = department_employee;
	}
	public int getDepartment_professor() {
		return department_professor;
	}
	public void setDepartment_professor(int department_professor) {
		this.department_professor = department_professor;
	}


}
