package net.skhu.dto;

public class Student {
	int student_id;
	String student_name;
	String student_address;
	String student_phone;
	int student_major;
	int student_minor;
	int survey_check;

	public int getStudent_id() {
		return student_id;
	}
	public void setStudent_id(int student_id) {
		this.student_id = student_id;
	}
	public String getStudent_name() {
		return student_name;
	}
	public void setStudent_name(String student_name) {
		this.student_name = student_name;
	}
	public String getStudent_address() {
		return student_address;
	}
	public void setStudent_address(String student_address) {
		this.student_address = student_address;
	}
	public String getStudent_phone() {
		return student_phone;
	}
	public void setStudent_phone(String student_phone) {
		this.student_phone = student_phone;
	}
	public int getStudent_major() {
		return student_major;
	}
	public void setStudent_major(int student_major) {
		this.student_major = student_major;
	}
	public int getStudent_minor() {
		return student_minor;
	}
	public void setStudent_minor(int student_minor) {
		this.student_minor = student_minor;
	}
	public int getSurvey_check() {
		return survey_check;
	}
	public void setSurvey_check(int survey_check) {
		this.survey_check = survey_check;
	}


}
